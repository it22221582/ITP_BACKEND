import mongoose from "mongoose";

const Schema = mongoose.Schema;

const adminSchema = new Schema({
  firstName: {
    type: String,
    required: true,
  },
  lastName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  mobile: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  role: {
    type: String,
    required: true,
    default: "ADMIN",
  },
});

const Admin = mongoose.model("Admin", adminSchema);

// module.exports = Admin;

export default Admin;
